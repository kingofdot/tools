def run():
    with open(JSON_PATH, encoding='utf-8') as f:
        data = json.load(f)
    items = data.get('별표내용', [])

    added = 0
    fixed = 0

    # 1) null 항목 추가
    for idx, codes in WASTE_CODE_UPDATES.items():
        if idx >= len(items):
            print(f'[skip] idx={idx} 범위 초과')
            continue
        item = items[idx]
        if not isinstance(item, dict):
            continue
        tags = item.get('tags')
        if tags is None:
            item['tags'] = {'wasteCode': codes}
            added += 1
        else:
            old = tags.get('wasteCode')
            tags['wasteCode'] = codes
            if old is None:
                added += 1
            else:
                print(f'[overwrite] idx={idx}: {old} → {codes}')

    # 2) 기존 오류 코드 수정
    for idx, codes in EXISTING_CODE_FIXES.items():
        if idx >= len(items):
            continue
        item = items[idx]
        if not isinstance(item, dict):
            continue
        tags = item.get('tags')
        if tags is None:
            continue
        old = tags.get('wasteCode')
        if old == codes:
            continue
        tags['wasteCode'] = codes
        fixed += 1
        print(f'[fix] idx={idx}: {old} → {codes}')

    with open(JSON_PATH, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f'\n완료: wasteCode 추가 {added}건, 기존 오류 수정 {fixed}건')


if __name__ == '__main__':
    run()
